// Crie um vetor contendo cinco nomes e exiba cada um deles utilizando um laço de repetição.
void main() {
  List<String> nomes = ['João', 'Maria', 'Pedro', 'Ana', 'Carlos'];

  for (var nome in nomes) {
    print(nome);
  }
}

